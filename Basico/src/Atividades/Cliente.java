package Atividades;

public class Cliente {
	
	String nome;
	String departamento;
	Double salario;
	String dataBanco;
	String rg;

	public Cliente(String nome, String departamento,  Double salario, String dataBanco, String rg ) {
		super();
		this.nome = nome;
		this.departamento = departamento;
		this.salario = salario;
		this.dataBanco = dataBanco;
		this.rg = rg;
	}
	
}

